package com.zx.bean;

import java.io.Serializable;

public class Sellman implements Serializable {

	private static final long serialVersionUID = 1L;
	private int id = 0;
	private int comment_id = 0;
	private String name = "";
	private String tel = "";
	private String photo = "";
	private String address = "";
	private String keyword;
	private float ratnum =0;
	private double lat;
	private double log;

	public Sellman() {

	}

	public Sellman(String name, String keyword, double lat, double log) {
		super();
		this.name = name;
		this.keyword = keyword;
		this.lat = lat;
		this.log = log;
	}

	public Sellman(String logo, int id, String password, String name,
			String tel, String photo, String address, String keyword,
			double lat, double log) {
		super();
		this.id = id;
		this.name = name;
		this.tel = tel;
		this.photo = photo;
		this.address = address;
		this.keyword = keyword;
		this.lat = lat;
		this.log = log;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLog() {
		return log;
	}

	public void setLog(double log) {
		this.log = log;
	}

	@Override
	public String toString() {
		return "Sellman [id=" + id + ", name=" + name + ", tel=" + tel
				+ ", photo=" + photo + ", address=" + address + ", keyword="
				+ keyword + ", lat=" + lat + ", log=" + log + "]";
	}

	public int getComment_id() {
		return comment_id;
	}

	public void setComment_id(int comment_id) {
		this.comment_id = comment_id;
	}

	public float getRatnum() {
		return ratnum;
	}

	public void setRatnum(float ratnum) {
		this.ratnum = ratnum;
	}


}
